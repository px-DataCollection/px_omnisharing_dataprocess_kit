import json
import os


def save_coords_to_json(coords_data, part_name, output_dir):
    """将坐标数据保存为JSON和二进制格式"""
    os.makedirs(output_dir, exist_ok=True)

    # 保存JSON格式
    json_path = os.path.join(output_dir, f"{part_name}.json")
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(coords_data, f, indent=2)
        print(f"已保存JSON: {json_path}")

    # 尝试保存msgpack格式
    try:
        import msgpack

        msgpack_path = os.path.join(output_dir, f"{part_name}.msgpack")
        with open(msgpack_path, "wb") as f:
            msgpack.pack(coords_data, f)
            print(f"已保存msgpack: {msgpack_path}")
    except ImportError:
        print("提示: 如需保存二进制格式,请安装 msgpack: pip install msgpack-python")


def save_merged_coords(coords_mapping, output_dir):
    """将所有坐标数据合并保存为一个文件"""
    # 创建输出目录
    os.makedirs(output_dir, exist_ok=True)

    # 保存合并的JSON格式
    json_path = os.path.join(output_dir, "grid_coords.json")
